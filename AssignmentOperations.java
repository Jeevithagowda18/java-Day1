package Jeevitha;

public class AssignmentOperations {
    public static void main(String[] args) {
    	int X = 10;
    	System.out.println("X = "+X);
    	X+=20;
    	System.out.println("X = " +X);
    	X-=2;
    	System.out.println("X = " +X);
    	X*=2;
    	System.out.println("X = " +X);
    }
}
