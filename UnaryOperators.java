package Jeevitha;

public class UnaryOperators {

	public static void main(String[] args) {
		int x=5;
		int y=x++;
		int z=++x;
		System.out.println("x="+x);
		System.out.println("y="+y);
		System.out.println("z="+z);
		
		int m=10;
		int n=m--;
		int o=--m;
		System.out.println("m="+m);
		System.out.println("n="+n);
		System.out.println("o="+o);
	}
}
