package Jeevitha;

public class RelationalOperator {

	public static void main(String[] args) {
		int x = 10;
		int y = 20;
		System.out.println(x<y);
		System.out.println(x<=y);
		System.out.println(y>50);
		System.out.println(y>10);
		System.out.println(x==y);
		System.out.println(x!=y);
	}
}
